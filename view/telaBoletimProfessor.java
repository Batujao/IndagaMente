    private void carregarDados() {
    DefaultTableModel modelo = (DefaultTableModel) jTableHistorico.getModel();
    modelo.setRowCount(0); // Limpa tabela antes de adicionar

    for (Boletim boletim : professor.buscarBoletim()) {
        modelo.addRow(new Object[] {
            boletim.getNome(),
            boletim.getSobrenome(),
            boletim.getTituloAval(),
            boletim.getNotaFinal(),
            boletim.getStatus()
        });
    }
}

